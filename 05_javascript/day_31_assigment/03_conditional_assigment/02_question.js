// If marks >= 90 → “A grade”
// Else if marks >= 75 → “B grade”
// Else if marks >= 50 → “C grade”
// Else → “Fail”


let marks = 5;
if(marks >= 90){
    console.log("A grade");   
}else if(marks >= 75){
    console.log("B grade");  
}else if(marks >= 50){
    console.log("C grade"); 
}else{
    console.log("Fail"); 
}